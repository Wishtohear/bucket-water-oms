-- 显示订单列表
SELECT o.id, o.order_no, o.status, o.total_amount, o.payment_type, o.create_time,
       s.name AS station_name, w.name AS warehouse_name
FROM orders o
LEFT JOIN station s ON o.station_id = s.id
LEFT JOIN warehouse w ON o.warehouse_id = w.id
ORDER BY o.create_time DESC;
